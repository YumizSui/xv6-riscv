uint64
sys_exit(void)
{
  int n;
  if(argint(0, &n) < 0)
    return -1;
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  if(argaddr(0, &p) < 0)
    return -1;
  return wait(p);
}

uint64
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

// 実行時間の計測: gettimeofdayを参考
uint64
sys_gettime(void)
{
  // 現在のticksを確保
  uint xticks;
  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);

  uint64 tv_addr;
  struct timeval tv;
  // gettimeの引数をポインタとして取得
  if(argaddr(0, &tv_addr) < 0)
    return -1;
  tv.tv_sec =xticks/10;
  tv.tv_msec = (xticks % 10)*100;

  // tvをkernel空間からuser空間にコピーする．
  if(copyout(myproc()->pagetable, tv_addr, (char *)&tv, sizeof(tv))  < 0)
    return -1;
  return 0;
}